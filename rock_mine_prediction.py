"""## 2.IMPORT LIBRARIES"""

import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score

"""## 3.READ DATA"""

rock_mine_data=pd.read_csv('sonar.csv',header=None)
rock_mine_copy=rock_mine_data.copy()
rock_mine_copy.head()

"""## 4.DATA UNDERSTANDING"""

rock_mine_copy.shape

rock_mine_copy.isna().sum()

rock_mine_copy.info()

rock_mine_copy.describe()

rock_mine_copy[60].value_counts()

"""# R ---> Rock
# M ---> Mine
"""

rock_mine_copy.groupby(60).mean()

"""## 5.MODEL BUILDING"""

x=rock_mine_copy.drop(columns=60,axis=1)
y=rock_mine_copy[60]

"""## 6.MODEL TESTING"""

x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=0.1,stratify=y,random_state=1)

print(x.shape,x_train.shape,x_test.shape)

"""## 7.MODEL TRAINING"""

model=LogisticRegression()

#model training with training data
model.fit(x_train,y_train)

"""## 7.MODEL EVALUATION"""

y_pred=model.predict(x_test)
df=pd.DataFrame({'Actual':y_test,'Predicted':y_pred})
df.head()

model_accuracy=accuracy_score(y_test,y_pred)
print('Accuracy on training data:',model_accuracy)

#Accuracy on training data
x_train_prediction=model.predict(x_train)
training_data_accuracy=accuracy_score(x_train_prediction,y_train)
print('Accuracy on training data:',training_data_accuracy)

#Accuracy on test data
x_test_prediction=model.predict(x_test)
test_data_accuracy=accuracy_score(x_test_prediction,y_test)
print('Accuracy on test data:',test_data_accuracy)

